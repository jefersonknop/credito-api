CREATE TABLE CLIENTE(
	ID				SERIAL PRIMARY KEY,
	NOME			VARCHAR (100),
	CPF				VARCHAR (14), 
	IDADE			INT,
	SEXO			VARCHAR(1),
	ESTADO_CIVIL	VARCHAR(30),
	ESTADO			VARCHAR(2),
	DEPENDENTES		INT,
	RENDA			FLOAT);
	
	
CREATE TABLE PROPOSTA(
	ID 				SERIAL PRIMARY KEY,
	CLIENTE			INT REFERENCES CLIENTE,
	ANALISE			VARCHAR(100),
	LIMITE_MINIMO	FLOAT,
	LIMITE_MAXIMO	FLOAT);
	

	
	

